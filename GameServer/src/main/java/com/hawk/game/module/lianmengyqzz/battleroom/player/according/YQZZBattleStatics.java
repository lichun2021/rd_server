public class YQZZBattleStatics {
	private int armyId;
	private int level;
	private int killCount;
	private int killCrossCount;
	private int hurtCount;
	private int hurtCrossCount;

	public int getArmyId() {
		return armyId;
	}

	public void setArmyId(int armyId) {
		this.armyId = armyId;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getKillCount() {
		return killCount;
	}

	public void setKillCount(int killCount) {
		this.killCount = killCount;
	}

	public int getKillCrossCount() {
		return killCrossCount;
	}

	public void setKillCrossCount(int killCrossCount) {
		this.killCrossCount = killCrossCount;
	}

	public int getHurtCount() {
		return hurtCount;
	}

	public void setHurtCount(int hurtCount) {
		this.hurtCount = hurtCount;
	}

	public int getHurtCrossCount() {
		return hurtCrossCount;
	}

	public void setHurtCrossCount(int hurtCrossCount) {
		this.hurtCrossCount = hurtCrossCount;
	}

}
