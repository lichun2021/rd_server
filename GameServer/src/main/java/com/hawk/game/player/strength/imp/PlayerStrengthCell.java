package com.hawk.game.player.strength.imp;

public class PlayerStrengthCell {
	
	/**
	 * 攻击加成
	 */
	private int atk;
	
	/**
	 * 血量加成
	 */
	private int hp;

	public int getAtk() {
		return atk;
	}

	public void setAtk(int atk) {
		this.atk = atk;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}
}
